CREATE TABLE Employees (
    EmpID VARCHAR(20) PRIMARY KEY,
    Age INT,
    AgeGroup VARCHAR(10),
    Attrition VARCHAR(5),
    Department VARCHAR(50),
    Gender VARCHAR(10),
    MonthlyIncome DECIMAL(10,2),
    JobRole VARCHAR(100),
    YearsAtCompany INT,
    OverTime VARCHAR(5),
    WorkLifeBalance INT,
    JobSatisfaction INT,
    PerformanceRating INT,
    DiversityFlag VARCHAR(20)
);
